
package javacore.operator;

/**
 * class này giới thiệu về các toán tử gán trong java
 * @author HS
 */
public class PhepGan {
    /**
     * method main
     */
    public static void main(String[] args) {
        //tạo biến a và gán giá trị bằng 20
        int a = 20;
        System.out.println("a1 = "+a);
        
        //phép gán +=
        a+=1;
        System.out.println("a2 = "+a);
        
        //phép gán -=
        a-=1;
        System.out.println("a3 = "+a);
        
        //phép gán *=
        a*=2;
        System.out.println("a4 = "+a);
    }
}
