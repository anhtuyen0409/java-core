
package javacore.operator;

/**
 * class này giới thiệu về các toán tử quan hệ trong java 
 * @author HS
 */
public class RelationshipOperators {
    /**
     * method main
     */
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println("a = b: "+(a==b));
        System.out.println("a != b: "+(a!=b));
        System.out.println("a > b: "+(a>b));
        System.out.println("a < b: "+(a<b));
        System.out.println("a >= b: "+(a>=b));
        System.out.println("a <= b: "+(a<=b));
    }
}
